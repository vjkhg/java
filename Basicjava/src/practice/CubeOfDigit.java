package practice;

public class CubeOfDigit {
	  //cube of a digit using static method
		  static int cube(int x)
		  {  
		  return x*x*x;  
		  }  
		  
		  public static void main(String args[])
		  {  
		  int result=CubeOfDigit.cube(5);  
		  System.out.println(result);  
		  
		  }
		  
		  }
		


