package practice;

public class DecrementOperatorExample {
	public static void main(String args[]){
		int i= 5;
		System.out.println(i);//5
		System.out.println(--i);//4
		System.out.println(i);//4
		System.out.println(i--);//4
		System.out.println(i);//3
		System.out.println(--i + i--);//2 + 2=4
		System.out.println(i);//1
		
	}

}
